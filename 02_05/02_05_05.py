t = input()
t = t.lower()
if 'жители' in t:
    print('Изумрудная страна')
else:
    if 'жевуны' in t:
        print('Голубая страна')
    else:
        if 'мигуны' in t:
            print('Фиолетовая страна')
        else:
            if 'болтуны' in t:
                print('Розовой страны')
            else:
                if 'крикуны' in t:
                    print('Жолтая страна')
                else:
                    print('Нет страны')